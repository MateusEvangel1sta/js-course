const number = 10;
const otherNumber = 2;

// Addition
console.log(number + otherNumber);
// Subtraction
console.log(number - otherNumber);
// Multiplication
console.log(number * otherNumber);
// Division
console.log(number / otherNumber);
// Power
console.log(number ** otherNumber);
// Rest of the division
console.log(number % otherNumber); // return 0

let counter = 1;
counter++; // increment
console.log(counter);

counter--; // decrement
console.log(counter);

// Assignment operations
counter += 2;
console.log(counter);

counter -= 2;
console.log(counter);

counter *= 10;
console.log(counter);